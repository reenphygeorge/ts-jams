const sum = (num1: number, num2: number) => {
    return num1 + num2;
};

const subtract = (num1: number, num2: number) => {
    return num1 - num2;
};

const multiply = (num1: number, num2: number) => {
    return num1 * num2;
};

const divide = (num1: number, num2: number) => {
    return num1 / num2;
};

export { sum, subtract, multiply, divide }